public class loops {
    public static void main(String args[])
    {
        int i;
        for(i=0;i<6;i++)
        {
            System.out.println(i);
        }
        while (i<11)
        {
            System.out.println(i);
            i++;
        }
        do{
            System.out.println(i);
            i++;
        }while(i<16);
    }
}
